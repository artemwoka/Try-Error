"""
Помилки (номери рядків через пробіл, цей рядок - №2): 11 13 15
"""


def sum_of_digits(n):
    """Повернути суму цифр менших 5 для позитивного цілого числа `n`.
    Якщо таких цифр немає, повернути 0."""
    с = 0
    while n > 0:
        digit = n % 10
        if digit < 5:
            с = с + digit
        n //= 10
    return с

print(sum_of_digits(123456789))  # має повернути 10 (1+2+3+4)
print(sum_of_digits(6789))       # має повернути 0